    public void cargar_conexiones() {
        LinkedList<Conexion> lista = conexion.obtener_conexiones();
        String[] conexiones = new String[lista.size()+1];
        conexiones[0] = "--- Seleccione una conexión ---";
        for (int i=0; i<lista.size(); i++) {
            Conexion conex = (Conexion) lista.get(i);
            conexiones[i+1] = conex.getModulo();
        }
        this.conexion_jComboBox.setModel(new javax.swing.DefaultComboBoxModel( conexiones ));
        this.conexion_jComboBox.addActionListener( new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                LinkedList<Conexion> lista_conexiones = conexion.obtener_conexiones();
                String modulo_ = (String) ((JComboBox)e.getSource( )).getSelectedItem();
                
                for (int i=0; i<lista_conexiones.size(); i++) {
                    Conexion objeto = (Conexion) lista_conexiones.get(i);
                    
                    if (objeto.getModulo().compareTo(modulo_) == 0) {
                        id_conexion = objeto.getId_conexion();
                        modulo.setText(objeto.getModulo());
                        base_datos.setText(objeto.getBase_datos());
                        usuario_bd.setText(objeto.getUsuario());
                        clave_bd.setText(objeto.getClave());
                        break;
                    } else {
                        id_conexion = null;
                        modulo.setText("");
                        base_datos.setText("");
                        usuario_bd.setText("");
                        clave_bd.setText("");
                    }
                }
            }
        } );
    }
    
