    private void eliminar_conexion_jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminar_conexion_jButtonActionPerformed
        if (modulo.getText().compareTo("") != 0 && base_datos.getText().compareTo("") != 0 && usuario_bd.getText().compareTo("") != 0)
            if (id_conexion != null) {
                JOptionPane.showMessageDialog(this, conexion.eliminar_conexion(id_conexion.toString()));
                cargar_conexiones();
            } else
                JOptionPane.showMessageDialog(this, "No ha seleccionado dato a eliminar.");
        else
            JOptionPane.showMessageDialog(this, "No ha seleccionado dato a eliminar.");
}//GEN-LAST:event_eliminar_conexion_jButtonActionPerformed

    private void actualizar_conexion_jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizar_conexion_jButtonActionPerformed
        if (modulo.getText().compareTo("") != 0 && base_datos.getText().compareTo("") != 0 && usuario_bd.getText().compareTo("") != 0)
            if (id_conexion != null) {
                JOptionPane.showMessageDialog(this, conexion.actualizar_conexion(id_conexion.toString(), modulo.getText(), base_datos.getText(), usuario_bd.getText(), clave_bd.getText()));
                cargar_conexiones();
            } else
                JOptionPane.showMessageDialog(this, "No ha seleccionado dato a actualizar.");
        else
            JOptionPane.showMessageDialog(this, "Todos los campos son necesarios.");
}//GEN-LAST:event_actualizar_conexion_jButtonActionPerformed

